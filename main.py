# 4. SIMULATION EXECUTION & ANIMATION
# ==========================================
class DynamicAgent:
    def __init__(self, id, start, goal, color):
        self.id = id
        self.pos = np.array(start, dtype=float)
        self.goal = np.array(goal, dtype=float)
        self.color = color
        self.global_path = None
        self.path_idx = 1 
        self.history = [self.pos.copy()]
        
        self.mode = 1           
        self.rand_angle = 0.0 
        self.mode_history = [self.mode] 

def get_current_target(agent):
    """
    Dynamic Lookahead Algorithm (Pure Pursuit style).
    Always searches for the closest point on the global path relative to the agent's 
    CURRENT physical position, then looks slightly ahead. This guarantees recovery 
    even if Chaos Mode blasts the agent far away from its expected route.
    """
    dists = np.linalg.norm(agent.global_path - agent.pos, axis=1)
    closest_idx = np.argmin(dists)
    
    # Ensure the agent tightly tracks the safe RRT* centerline
    # through the door and avoids cutting into the wall corners.
    target_idx = min(closest_idx + 3, len(agent.global_path) - 1)
    return agent.global_path[target_idx]

def run_simulation():
    # THE STRESS TEST: 1.5m Corridor
    obstacles = [
        (4.8, 5.75, 0.4, 6.25),   # Top wall pulled down
        (4.8, -2.0, 0.4, 6.25),   # Bottom wall pulled up
        (-2.0, 11.0, 14.0, 0.5),  
        (-2.0, -1.5, 14.0, 0.5),  
        (-1.5, -1.5, 0.5, 13.0),  
        (11.0, -1.5, 0.5, 13.0)   
    ]
    
    a1 = DynamicAgent(1, [0.0, 5.0], [10.0, 5.0], 'blue')
    a2 = DynamicAgent(2, [10.0, 5.0], [0.0, 5.0], 'red')
    agents = [a1, a2]
    
    print("Computing Initial RRT* Bridge Paths...")

    # MINKOWSKI SUM
    rrt_obstacles = [
        (x - ROBOT_RADIUS, y - ROBOT_RADIUS, w + (2 * ROBOT_RADIUS), h + (2 * ROBOT_RADIUS)) 
        for (x, y, w, h) in obstacles
    ]

    for a in agents:
        # Pass C-space obstacles to the global planner
        rrt = RRTStarBridge(a.pos, a.goal, rrt_obstacles, [-2, 12])
        raw_path = rrt.plan()
        if raw_path is None:
            raw_path = np.array([a.pos, a.goal])
        # Smooth the raw RRT* waypoints
        a.global_path = smooth_global_path(raw_path, num_points=100)

    print("Running Background Simulation...")
    apf = TwoModeAPF(r_base=ROBOT_RADIUS) # Pass global robot physics here too
    dt = 0.5 
    
    # Simulation Receding Horizon Loop
    for step in range(800): 
        
        # EXACT TERMINATION MATCH
        # Evaluate if the entire system has reached its goal state exactly at the 
        # GOAL_TOLERANCE margin.
        all_parked = all(np.linalg.norm(a.pos - a.goal) <= GOAL_TOLERANCE for a in agents)
        
        # EARLY TERMINATION TRIGGER
        # Stop simulating the moment both agents are perfectly parked at their targets.
        if all_parked:
            print(f"Simulation completed successfully! Both agents parked at step {step}.")
            break
            
        for i, agent in enumerate(agents):
            dist_to_final_goal = np.linalg.norm(agent.pos - agent.goal)
            
            # If the robot is within tolerance, just engage the parking brake. 
            # It retains its true kinematic coordinate without faking the position.
            if dist_to_final_goal <= GOAL_TOLERANCE:
                agent.history.append(agent.pos.copy())
                agent.mode_history.append(1)
                continue 
                
            # Keep ALL agents in the obstacle array. A parked agent represents a solid,
            # unmoving physical robot that the remaining agents must navigate around.
            other_agents = [a for a in agents if a.id != agent.id]
            
            target_wp = get_current_target(agent)
            
            # Calculate ideal APF force
            v_des = apf.get_desired_velocity(agent, target_wp, other_agents, obstacles)
            
            # By-pass the B-spline entirely for perfect 0.0 margin zero-velocity stops
            if np.linalg.norm(v_des) == 0:
                agent.history.append(agent.pos.copy())
                agent.mode_history.append(1)
                continue
                
            # Feed the APF velocity into the Tube B-Spline optimizer for collision-safe smoothing
            # Ensure the Tube uses the exact same physical radius!
            short_term_goal = agent.pos + (v_des * dt * 2.0) 
            planner = TubeBSplineShort(agent.pos, short_term_goal, obstacles, r0=ROBOT_RADIUS)
            safe_traj = planner.plan()
            
            # Extract the actual safe step from the B-spline path execution
            agent.pos = safe_traj[1] 
            agent.history.append(agent.pos.copy())
            agent.mode_history.append(agent.mode)

    print("Rendering Animation...")
    fig, ax = plt.subplots(figsize=(8, 8)) 
    
    # Draw physical obstacles
    for (ox, oy, w, h) in obstacles[:2]:
        ax.add_patch(patches.Rectangle((ox, oy), w, h, color='black', alpha=0.8))
        
    # Draw paths and target stars
    for a in agents:
        ax.plot(a.global_path[:,0], a.global_path[:,1], color=a.color, linestyle=':', alpha=0.3)
        ax.scatter(a.history[0][0], a.history[0][1], marker='o', s=100, color=a.color, edgecolors='black', zorder=4)
        
        # The star is drawn at `a.goal`
        ax.scatter(a.goal[0], a.goal[1], marker='*', s=300, color='yellow', edgecolors=a.color, linewidths=2, zorder=5)

    ax.set_title("Stochastic Symmetry Breaking: 2-Mode Hybrid APF", fontsize=14)
    ax.set_xlim(-1.0, 11.0)
    ax.set_ylim(-1.0, 11.0)
    ax.set_aspect('equal')
    plt.grid(True, linestyle='--', alpha=0.6)

    # Dynamic text box for tracking mode changes
    mode_text = ax.text(0.5, 0.95, '', transform=ax.transAxes, ha='center', fontsize=12, 
                        bbox=dict(facecolor='white', alpha=0.8, edgecolor='black'), zorder=10)
    
    # Setup animation objects
    lines = []
    heads = []
    for a in agents:
        line, = ax.plot([], [], color=a.color, lw=2.5, zorder=3)
        head, = ax.plot([], [], 'o', color=a.color, markersize=10, markeredgecolor='white', zorder=5)
        lines.append(line)
        heads.append(head)

    def update(frame):
        for i, a in enumerate(agents):
            hist = np.array(a.history[:frame+1])
            lines[i].set_data(hist[:, 0], hist[:, 1])
            heads[i].set_data([hist[-1, 0]], [hist[-1, 1]])

        m1_str = "Chaos" if agents[0].mode_history[frame] == 2 else "Stable"
        m2_str = "Chaos" if agents[1].mode_history[frame] == 2 else "Stable"
        mode_text.set_text(f"Step: {frame} | Agent 1: {m1_str} | Agent 2: {m2_str}")
        return lines + heads + [mode_text]

    max_frames = max(len(a.history) for a in agents)
    ani = FuncAnimation(fig, update, frames=max_frames, blit=False, interval=100, repeat=False)
    
    plt.show()

if __name__ == "__main__":
    run_simulation()
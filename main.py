# 3. 2-MODE HYBRID APF (Pure State Machine)
# ==========================================
class TwoModeAPF:
    """
    A Hybrid Artificial Potential Field that governs the high-level decision making.
    It evaluates environmental forces to detect deadlocks, and switches between a 
    Stable Mode (Gradient Descent) and a Chaos Mode (Stochastic Symmetry Breaking).
    """
    def __init__(self, k_att=2.0, k_rep_agent=5.0, k_rep_wall=8.0, r_base=ROBOT_RADIUS, kappa=0.5, deadlock_tol=0.2):
        # Force Coefficients
        self.k_att = k_att               # Attraction strength toward the goal
        self.k_rep_agent = k_rep_agent   # Repulsion strength from other agents
        self.k_rep_wall = k_rep_wall     # Repulsion strength from walls/obstacles
        
        # Dynamic Safety Tube parameters
        self.r_base = r_base             # Base radius of the robot (tied to global config)
        self.kappa = kappa               # Velocity-dependent radius scaling factor
        
        # Deadlock Detection
        self.deadlock_tol = deadlock_tol # Threshold for detecting a loss of forward progress
        self.d_safe_wall = 0.6           # The physical size of the wall's repulsive forcefield

    def get_closest_point_on_rect(self, pos, rect):
        """Calculates the mathematically nearest geometric point on an Axis-Aligned Bounding Box (AABB)"""
        rx, ry, rw, rh = rect
        cx = np.clip(pos[0], rx, rx + rw)
        cy = np.clip(pos[1], ry, ry + rh)
        return np.array([cx, cy])

    def get_desired_velocity(self, agent, p_target, other_agents, obstacles):
        p_i = agent.pos

        # DECOUPLE STEERING FROM BRAKING
        # dist_to_final_goal strictly controls the brakes (distance to absolute final target)
        dist_to_final_goal = np.linalg.norm(agent.goal - p_i)

        ## ---------------------------------------------------------
        # PHASE 1: ATTRACTION & PERFECT PARKING
        # ---------------------------------------------------------
        # 1. Engage brakes if within Goal Tolerance. No teleporting.
        if dist_to_final_goal <= GOAL_TOLERANCE:
            return np.zeros(2)

        # 2. Linear damping: slow down dynamically as we approach the target
        # This acts as a kinematic brake to prevent overshooting the final coordinate.
        v_max = 1.5
        arrival_radius = 1.0
        target_speed = v_max * (dist_to_final_goal / arrival_radius) if dist_to_final_goal < arrival_radius else v_max
        
        # 3. GENERALIZED STEERING (No Map-Specific Hardcoding)
        # We always steer toward the lookahead waypoint (p_target). 
        # Because the lookahead function clamps to the end of the array, 
        # p_target naturally becomes the absolute goal at the end of the journey.
        dir_to_waypoint = p_target - p_i
        dist_to_waypoint = np.linalg.norm(dir_to_waypoint)
        
        # Steer towards waypoint at the speed dictated by the final goal distance
        dir_target_norm = dir_to_waypoint / dist_to_waypoint if dist_to_waypoint > 0 else np.zeros(2)
        v_att = dir_target_norm * target_speed

        # ---------------------------------------------------------
        # PHASE 2: HYPER-STIFF CUBIC REPULSION
        # ---------------------------------------------------------
        # Calculate dynamic safety distance based on the agent's previous step speed
        current_speed = 0.0
        if len(agent.history) > 1:
            current_speed = np.linalg.norm(agent.history[-1] - agent.history[-2]) / 0.5
        dynamic_d_safe = self.r_base + (self.kappa * current_speed)

        v_rep = np.zeros(2)
        
        # Agent-to-Agent Repulsion (Cubic formulation)
        # Guarantees mathematical divergence (infinite repulsion) as agents approach collision.
        for other in other_agents:
            dist_ij = np.linalg.norm(p_i - other.pos)
            if 0.01 < dist_ij < dynamic_d_safe:
                rep_mag = self.k_rep_agent * ((1.0/dist_ij - 1.0/dynamic_d_safe)**3) * (1.0/(dist_ij**2))
                v_rep += rep_mag * ((p_i - other.pos) / dist_ij)

        # Agent-to-Wall Repulsion (Cubic formulation)
        for obs in obstacles:
            closest_pt = self.get_closest_point_on_rect(p_i, obs)
            dist_wall = np.linalg.norm(p_i - closest_pt)
            if 0.01 < dist_wall < self.d_safe_wall:
                rep_mag = self.k_rep_wall * ((1.0/dist_wall - 1.0/self.d_safe_wall)**3) * (1.0/(dist_wall**2))
                v_rep += rep_mag * ((p_i - closest_pt) / dist_wall)

        # THE "PARKING PANIC" FIX
        # Calculate theoretical progress using UN-DAMPED attraction.
        # This isolates environmental resistance (walls/agents) from deliberate parking brakes.
        v_att_undamped = dir_target_norm * v_max
        theoretical_forward_progress = np.dot(v_att_undamped + v_rep, dir_target_norm)

        # ---------------------------------------------------------
        # PHASE 3: PURE GEOMETRIC STATE MACHINE
        # ---------------------------------------------------------
        if agent.mode == 1:
            # TRIGGER CHAOS: If forward progress drops below tolerance, we are trapped.
            if theoretical_forward_progress < self.deadlock_tol:
                agent.mode = 2
                agent.rand_angle = np.random.uniform(0, 2 * np.pi) 
                
        elif agent.mode == 2:
            # BULLETPROOF HYSTERESIS
            # Max possible progress is 1.5. We demand > 1.4 to exit Chaos.
            # The agent MUST bounce until it is completely clear of the obstacle's
            # repulsive forcefield to prevent rapid ping-ponging at corners.
            if theoretical_forward_progress > 1.4:
                agent.mode = 1

        # ---------------------------------------------------------
        # PHASE 4: VELOCITY CALCULATION
        # ---------------------------------------------------------
        v_final = np.zeros(2)

        if agent.mode == 1:
            # Mode 1 (Stable): Standard APF Gradient Tracking
            v_final = v_att + v_rep
        else:
            # Mode 2 (Chaos): Stochastic Symmetry Breaking
            # Injects random directional velocity while maintaining wall repulsion safety
            agent.rand_angle += np.random.uniform(-0.5, 0.5)
            rand_mag = np.random.uniform(2.0, 4.0) 
            v_rand = np.array([np.cos(agent.rand_angle), np.sin(agent.rand_angle)]) * rand_mag
            v_final = v_rand + v_rep
            
        # Ensure we never exceed our dynamically damped target speed limit
        speed = np.linalg.norm(v_final)
        if speed > target_speed:
            v_final = (v_final / speed) * target_speed

        return v_final

